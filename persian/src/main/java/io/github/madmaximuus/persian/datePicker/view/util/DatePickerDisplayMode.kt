/**
 * Enum class representing the different display modes of the date picker.
 *
 * @property CALENDAR The calendar display mode.
 * @property MONTH The month display mode.
 * @property YEAR The year display mode.
 */
internal enum class DatePickerDisplayMode {
    CALENDAR, MONTH, YEAR,
}