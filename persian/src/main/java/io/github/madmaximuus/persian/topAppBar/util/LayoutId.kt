package io.github.madmaximuus.persian.topAppBar.util

/**
 * An internal object that defines constants for layout identifiers.
 *
 * These identifiers are used to refer to specific layout components within the application.
 *
 * @property LEFT The identifier for the left component.
 * @property RIGHT_ICON The identifier for the right icon component.
 * @property RIGHT_BUTTON The identifier for the right button component.
 * @property RIGHT_OVERFLOW The identifier for the right overflow component.
 * @property TITLE The identifier for the title component.
 */
internal object LayoutId {
    const val LEFT = "left"
    const val RIGHT_ICON = "right_icon"
    const val RIGHT_BUTTON = "right_button"
    const val RIGHT_OVERFLOW = "right_overflow"
    const val TITLE = "title"
}